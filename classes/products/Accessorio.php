<?php 

class Accessorio {
    private $nome;
    private $prezzo;
    private $materiale;
    private $dimensioni;

    public function __construct($prezzo, $materiale, $dimensioni) {
        $this->nome = $nome;
        $this->prezzo = $prezzo;
        $this->materiale = $materiale;
        $this->dimensioni = $dimensioni;
    }
}